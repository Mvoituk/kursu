<h1>About</h1>
<p>PHP — мова, у код якої можна вбудовувати безпосередньо html-код сторінок, які, у свою чергу, коректно оброблюватимуться PHP-інтерпретатором.</p>
<p>Обробник PHP просто починає виконувати код після відкриваючого тегу  і продовжує виконання до того моменту, поки не зустріне закриваючий тег.</p>
<p>Велика різноманітність функцій PHP дає можливість уникати написання багаторядкових функцій, призначених для користувача, як це відбувається в C або Pascal.</p>
